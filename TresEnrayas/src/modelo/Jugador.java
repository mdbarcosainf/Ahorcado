public class Jugador {
    private String nombre;
    private char marca;

    public Jugador(String nombre, char marca) {
        this.nombre = nombre;
        this.marca = marca;
    }

    public String getNombre() {
        return nombre;
    }

    public char getMarca() {
        return marca;
    }
}






